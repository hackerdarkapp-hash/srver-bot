def get_security_tip(tip_type: str = "daily") -> str:
    if tip_type == "daily":
        # نصيحة اليوم بناءً على رقم اليوم
        day_index = datetime.now().timetuple().tm_yday % len(DAILY_TIPS)
        title, tip = DAILY_TIPS[day_index]
        random_tips = random.sample(DAILY_TIPS, min(3, len(DAILY_TIPS)))
        extra = "\n".join(f"  • {t[0]}: {t[1][:60]}..." for t in random_tips if t[0] != title)
        return (
            f"📚 <b>نصيحة أمنية اليوم</b>\n"
            f"{'━' * 22}\n\n"
            f"{title}\n"
            f"💡 {tip}\n\n"
            f"<b>نصائح إضافية:</b>\n{extra}\n\n"
            f"🔁 ارجع غداً لنصيحة جديدة!"
        )
    elif tip_type in CATEGORY_TIPS:
        tips = CATEGORY_TIPS[tip_type]
        cat_name = CATEGORIES.get(tip_type, tip_type)
        selected = random.sample(tips, min(4, len(tips)))
        lines = [
            f"📚 <b>{cat_name}</b>",
            f"{'━' * 22}",
            "",
        ]
        for i, tip in enumerate(selected, 1):
            lines.append(f"<b>{i}.</b> {tip}")
            lines.append("")
        lines.append("⚠️ <i>طبّق هذه النصائح لحماية نفسك رقمياً.</i>")
        return "\n".join(lines)
    else:
        return get_security_tip("daily")


